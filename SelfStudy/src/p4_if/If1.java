package p4_if;

public class If1 {
	public static void main(String[] args) {
		int age = 7;
		
		if(age >= 8){
			System.out.println("go to school");
		}
		else{
			System.out.println("stay home");
		}
		
		
		char gender = 'F';
		if (gender == 'F') {
			System.out.println("Woman");
		}
		else {
			System.out.println("man");
		}
	}
}
