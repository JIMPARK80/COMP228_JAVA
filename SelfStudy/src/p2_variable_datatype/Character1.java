package p2_variable_datatype;

public class Character1 {

	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println(ch1); // output character
		System.out.println((int)ch1); // output value of character
		
		char ch2 = 66; // input integer
		System.out.println(ch2); // output character of the integer value
		
		int ch3 = 67;
		System.out.println(ch3); // output value of character
		System.out.println((char)ch3); // output character of the integer value
		
		char ch4 = 'Z';
		System.out.println(ch4);
		System.out.println((int)ch4);
		
		char ch5 = 38;
		System.out.println(ch5);
		
		int ch6 = 97;
		System.out.println(ch6);
		System.out.println((char)ch6);
	}
}
