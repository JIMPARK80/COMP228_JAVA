package p2;

public class Character1 {

	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println(ch1); // print character
		System.out.println((int)ch1); // print value of character
		
		char ch2 = 66; // input integer
		System.out.println(ch2); // print integer character
		
		int ch3 = 67;
		System.out.println(ch3);
		
	}

}
